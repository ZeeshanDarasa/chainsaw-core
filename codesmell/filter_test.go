package codesmell

import "testing"

func TestIsLikelyTestOrVendor(t *testing.T) {
	cases := []struct {
		path string
		want bool
	}{
		// Kept (real source).
		{"src/foo.js", false},
		{"lib/index.ts", false},
		{"package/main.py", false},
		{"app/server.go", false},
		// Vendored.
		{"node_modules/lodash/index.js", true},
		{"some/pkg/node_modules/x/y.js", true},
		{"vendor/github.com/foo/bar.go", true},
		{"python/site-packages/requests/api.py", true},
		{"proj/__pycache__/mod.cpython-310.pyc", true},
		// Tests.
		{"__tests__/bar.test.js", true},
		{"src/foo_test.go", true},
		{"app/foo.spec.ts", true},
		{"test/helper.js", true},
		{"tests/integration/x.py", true},
		{"spec/models/user_spec.rb", true},
		{"pkg/testdata/golden.json", true},
		{"app/fixtures/payload.json", true},
		// Build / dist.
		{"dist/bundle.js", true},
		{"build/output.js", true},
		{"web/.next/static/chunks/main.js", true},
		{"target/release/foo", true},
		// Examples / docs.
		{"examples/demo.js", true},
		{"docs/intro.md", true},
		// Case-insensitive + backslash normalisation.
		{"Node_Modules/x.js", true},
		{`pkg\__tests__\bar.test.js`, true},
		// Edge: empty.
		{"", false},
		// Edge: substring of a segment is NOT enough — must match boundaries.
		{"src/contest/foo.js", false},
		{"src/manifesto/foo.js", false},
	}
	for _, tc := range cases {
		got := IsLikelyTestOrVendor(tc.path)
		if got != tc.want {
			t.Errorf("IsLikelyTestOrVendor(%q) = %v, want %v", tc.path, got, tc.want)
		}
	}
}

