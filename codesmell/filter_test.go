func TestLooksGenerated(t *testing.T) {
	cases := []struct {
		name string
		body string
		want bool
	}{
		{"plain", "function foo() { return 1; }", false},
		{"at-generated", "// @generated by protoc\nfoo()\n", true},
		{"auto-generated-c", "/* AUTO-GENERATED FILE - do not edit */\n", true},
		{"hash-generated", "# Generated by Cargo\n[package]\n", true},
		{"html-generated", "<!-- Generated by docgen -->\n<html/>\n", true},
		{"empty", "", false},
		{"marker beyond probe", largeHead("// @generated", 1000), false},
		{"marker within probe", largeHead("// @generated", 100), true},
	}
	for _, tc := range cases {
		got := LooksGenerated([]byte(tc.body))
		if got != tc.want {
			t.Errorf("%s: LooksGenerated = %v, want %v", tc.name, got, tc.want)
		}
	}
}

// largeHead returns padding + marker, where padding is `pad` bytes of spaces
// followed by the marker. Used to verify we only inspect the head probe.
func largeHead(marker string, pad int) string {
	b := make([]byte, pad)
	for i := range b {
		b[i] = ' '
	}
	return string(b) + marker
}

func TestFilterTestVendorGenerated(t *testing.T) {
	in := map[string][]byte{
		"src/foo.js":                []byte("module.exports = 1\n"),
		"node_modules/x/index.js":   []byte("module.exports = 2\n"),
		"__tests__/bar.test.js":     []byte("test('x', () => {})\n"),
		"dist/bundle.js":            []byte("var a=1;a;"),
		"src/generated/proto.pb.go": []byte("// @generated by protoc\npackage gen\n"),
		"src/keep.go":               []byte("package keep\n"),
	}
	out := FilterTestVendorGenerated(in)

	mustHave := []string{"src/foo.js", "src/keep.go"}
	mustDrop := []string{
		"node_modules/x/index.js",
		"__tests__/bar.test.js",
		"dist/bundle.js",
		"src/generated/proto.pb.go",
	}
	for _, p := range mustHave {
		if _, ok := out[p]; !ok {
			t.Errorf("expected %q to be kept", p)
		}
	}
	for _, p := range mustDrop {
		if _, ok := out[p]; ok {
			t.Errorf("expected %q to be dropped", p)
		}
	}

	// Sanity: nil/empty input passes through.
	if got := FilterTestVendorGenerated(nil); got != nil {
		t.Errorf("nil input should pass through, got %v", got)
	}
}

// TestFilterAllowsMinifiedAndNativeTargets documents the contract that
// MinifiedCode and NativeBinary scanners must NOT use this filter — so we
// verify here that the would-be-excluded paths really are excluded by it
// (the scanner call sites are responsible for opting out).
func TestFilterAllowsMinifiedAndNativeTargets(t *testing.T) {
	// dist/bundle.js is the canonical minified target; vendor/lib.so is the
	// canonical native-binary-in-vendor target. Both are dropped by the
	// filter — which is why call sites for ScanMinified and ScanNativeBinary
	// must not apply it.
	if !IsLikelyTestOrVendor("dist/bundle.js") {
		t.Error("dist/bundle.js should be filtered (and so MinifiedCode must opt out)")
	}
	if !IsLikelyTestOrVendor("vendor/foo/lib.so") {
		t.Error("vendor/foo/lib.so should be filtered (and so NativeBinary must opt out)")
	}
}
